// reverse a string in JavaScript
// split() reverse() join()

//split()
const myString = "alex";
const stringSplit = myString.split('');
console.log(stringSplit);

// reverse()
const splitReversed = stringSplit.reverse();
console.log(splitReversed);

// join()
const reverseJoin = splitReversed.join('');
console.log(reverseJoin);